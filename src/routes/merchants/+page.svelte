>
    <MerchantFilters
        heroOptions={data.heroOptions}
        minimumTierOptions={data.minimumTierOptions}
        tagOptions={data.tagOptions}
        sizeOptions={data.sizeOptions}
        expansionOptions={data.expansionOptions}
        bind:selectedHeroes
        bind:selectedTiers
        bind:tagStates
        bind:selectedSizes
        bind:isMatchAnyTag
        bind:searchText
        bind:selectedExpansions
    />

    <MerchantList
        itemsServerVersion={data.itemsVersion}
        merchantsServerVersion={data.version}
